            'page_title'         => __("Recovery Saunas"),
        ];
        return view('Sauna::frontend.vendorSauna.index', $data);
    }

    public function restore($id)
    {
        $this->checkPermission('sauna_delete');
        $user_id = Auth::id();
        $query = $this->saunaClass::onlyTrashed()->where("create_user", $user_id)->where("id", $id)->first();
        if(!empty($query)){
            $query->restore();
        }
        return redirect(route('sauna.vendor.recovery'))->with('success', __('Restore sauna success!'));
    }

